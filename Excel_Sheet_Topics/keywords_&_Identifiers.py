# Python Keywords example
# Using keywords as variable names will raise a syntax error
# Uncommenting the following line will raise an error
# def = 10

# Identifiers example
# Valid identifiers
my_variable = 10
my_function_name = "hello"
myClass = "Python"

# Invalid identifiers (will raise syntax error)
# 1variable = 5  # Cannot start with a digit
# my-variable = 10  # Hyphens are not allowed in identifiers

# Using a keyword as a string
# This is valid because "class" is enclosed in quotes and treated as a string
my_string = "class"

# Print statements to demonstrate identifiers
print(my_variable)
print(my_function_name)
print(myClass)
print(my_string)

